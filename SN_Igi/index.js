var perceptron = function (digit) {
    this.digit = digit

    this.weights = new Array(currentSize*currentSize)
    this.teta = Math.random()

    this.getTheta = function (){
        return this.teta
    }

    this.countValue = function(digitTable){
        var result = 0.0
        for(var i = 0; i < currentSize*currentSize; i++)
        {
            result += parseFloat(digitTable[i] * this.weights[i])
        }
        return parseFloat(result)
    }

    this.perceptronLearning = function () {

        var scoreTable = new Array(10)
        for(var i = 0;i<10;i++){
            scoreTable[i] = 0
        }

        for (var i = 0; i < currentSize * currentSize; i++) {
            this.weights[i] = Math.random()
        }
        var sum = 0
        while(sum != 10) {
            sum = 0
            var random = parseInt(Math.random() * 10)
            var exVal = random == digit ? 1 : 0
            var ERROR = exVal - (this.countValue(digits[random][currentSize-5]) < this.teta ?0:1) // Tego już nie trobi
            if (ERROR == 0) {
                scoreTable[random] = 1
            }
            else {
                scoreTable[random] = 0
                for (var i = 0; i < currentSize * currentSize; i++) {
                    this.weights[i] = this.weights[i]  + n * ERROR * digits[random][currentSize-5][i]
                }
                this.teta = this.teta - n * ERROR
            }
            for(var i = 0; i < 10; i++) {
                sum += scoreTable[i]
            }
        }


    }

}

