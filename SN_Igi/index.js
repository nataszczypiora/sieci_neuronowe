function createGrid(size) {
    currentSize = size
    grid = new Array(size)
    for (i = 0; i < size; i++) {
        grid[i] = new Array(size)
    }
    mainContainer = document.getElementById("leftPane")
    while (mainContainer.firstChild) {
        mainContainer.firstChild.remove()
    }
    buttonTable = document.createElement("table")
    for (i = 0; i < size; i++) {
        var row = document.createElement("tr")
        for (j = 0; j < size; j++) {
            col = document.createElement("td")
            col.style = "width: " + (100 / size)
            bt = document.createElement("button")
            bt.id = i + "-" + j
            bt.onclick = function () {
                var x = this.id.charAt(0)
                var y = this.id.charAt(2)
                if (grid[x][y].getMarked() == "false") {
                    this.style = "background: #000000"
                    grid[x][y].setMarked("true")
                } else {
                    this.style = "background: aliceblue"
                    grid[x][y].setMarked("false")
                }
            }
            grid[i][j] = new field(i, j, "false", bt)
            col.appendChild(bt)
            row.appendChild(col)
        }
        buttonTable.appendChild(row)
    }
    mainContainer.appendChild(buttonTable)
    console.textContent = ""
    perceptrons = new Array(10)
    for (var i= 0; i<10;i++){
        perceptrons[i] = new perceptron(i)
        perceptrons[i].perceptronLearning()
    }
    console.textContent += "Perceptrony zostaly nauczone\n"
}

function clearGrid() {
    for (i = 0; i < currentSize; i++) {
        for (j = 0; j < currentSize; j++) {
            if (grid[i][j].getMarked() == "true") {
                grid[i][j].getBtn().click()
            }
        }
    }
}

function saveDigit() {
    console.textContent += "["
    for (i = 0; i < currentSize; i++) {
        for (j = 0; j < currentSize; j++) {
            if (grid[i][j].getMarked() == "true") {
                console.textContent += "1, "
            } else {
                console.textContent += "0, "
            }
        }
    }
    console.textContent += "]\n"
}

function randGrid() {
    var buf = 0.05
    for (i = 0; i < currentSize; i++) {
        for (j = 0; j < currentSize; j++) {
            if (Math.random() <= buf) {
                grid[i][j].getBtn().click()
                buf /= 2
            }
        }
    }
}

function getCurrentGrid() {
    var a = new Array(currentSize * currentSize)
    var k = 0
    for (i = 0; i < currentSize; i++) {
        for (j = 0; j < currentSize; j++) {
            if (grid[i][j].getMarked() == "true") {
                a[k] = 1
            } else {
                a[k] = 0
            }
            k++
        }
    }
    return a
}

function printDefaultDigit(num) {
    var s = currentSize - 5
    paintFromTab(digits[num][s])
}

function paintFromTab(tab) {
    clearGrid()
    var k = 0
    for (i = 0; i < currentSize; i++) {
        for (j = 0; j < currentSize; j++) {
            if (tab[k] == 1) {
                grid[i][j].getBtn().click()
            }
            k++
        }
    }
}

