function normalize(x) { // <- Tutaj mamy normalizację
    return parseFloat((x - xMin) / (xMax - xMin))
}

function module(a, b) {
    return Math.sqrt((a * a) + (b * b))
}

function fourier(in_array) {
    var len = in_array.length;
    var output = new Array();

    for (var k = 0; k < len; k++) {
        var real = 0;
        var imag = 0;
        for (var n = 0; n < len; n++) {
            real += in_array[n] * Math.cos(-2 * Math.PI * k * n / len);
            imag += in_array[n] * Math.sin(-2 * Math.PI * k * n / len);
        }
        output.push(normalize(module(real, imag)))
    }
    return output;
}

function addFourierTransform(array) {
    var sourceArray = new Array(furierSize[currentSize - 5])
    for (i = 0; i < furierSize[currentSize - 5]; i++) {
        if (i < currentSize * currentSize) {
            sourceArray[i] = array[i]
        } else {
            sourceArray[i] = 0
        }
    }
    var output = array.concat(fourier(sourceArray))
    return output
}

